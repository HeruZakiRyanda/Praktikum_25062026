    private void btnHargaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHargaActionPerformed
        // TODO add your handling code here:
        int kelas = (int)cbKelas.getSelectedIndex();

        if(!rbBkt.isSelected() && !rbSolok.isSelected() && !rbPar.isSelected()){
            JOptionPane.showMessageDialog(this, "Tujuan Belum Dipilih");
        }
        else if(cbKelas.getSelectedIndex()==0){
            JOptionPane.showMessageDialog(this,"Kelas Belum Dipilih");
        }
        else{

            switch(kelas){
                case 1 -> {
                    if(rbBkt.isSelected())
                    txtHarga.setText("40000");
                    else if(rbSolok.isSelected())
                    txtHarga.setText("25000");
                    else if(rbPar.isSelected())
                    txtHarga.setText("15000");
                }
                case 2 -> {
                    if(rbBkt.isSelected())
                    txtHarga.setText("75000");
                    else if(rbSolok.isSelected())
                    txtHarga.setText("55000");
                    else if(rbPar.isSelected())
                    txtHarga.setText("40000");
                    break;
                }
                case 3 -> {
                    if(rbBkt.isSelected())
                    txtHarga.setText("120000");
                    else if(rbSolok.isSelected())
                    txtHarga.setText("85000");
                    else if(rbPar.isSelected())
                    txtHarga.setText("60000");
                    break;
                }
            }

        }
    }//GEN-LAST:event_btnHargaActionPerformed

