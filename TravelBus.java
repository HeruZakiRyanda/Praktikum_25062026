    private void btnBayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBayarActionPerformed
        // TODO add your handling code here:
        int jumlah = Integer.parseInt(txtJumlah.getText());
        double harga = Double.parseDouble(txtHarga.getText());
        double total = jumlah*harga;
        double uang = Double.parseDouble(txtUang.getText());
        double kembali = uang - total;
        
        if(uang > total){
            kembali = uang - total;
            txtKembalian.setText(Double.toString(kembali));
        }
        else if(uang < total){
            JOptionPane.showMessageDialog(this, "Uang Anda Kurang", "Perhatian!!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnBayarActionPerformed

